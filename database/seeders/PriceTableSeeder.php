            $boxsizeModel = BoxSize::create($boxsize->except(['prices'])->toArray());

            $boxsize->only('prices')->each(function (array $priceData, string $key) use ($boxsizeModel): void {
                $prices = collect($priceData);

                $prices->each(
                    function (array $price, int $key) use ($boxsizeModel): void {
                        $temp = collect($price);

                        $priceModel = $boxsizeModel->prices()->create($temp->except(['items'])->toArray());

                        $priceModel->priceItems()->createMany($temp->only(['items'])->toArray()['items']);
                    }
                );
            });
        });
